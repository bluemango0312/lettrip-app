package com.cookandroid.travelerapplication.mypage;

public class TravelCount {
    String visited_count;
    String not_visited_count;

    public String getVisited_count() {
        return visited_count;
    }

    public void setVisited_count(String visited_count) {
        this.visited_count = visited_count;
    }

    public String getNot_visited_count() {
        return not_visited_count;
    }

    public void setNot_visited_count(String not_visited_count) {
        this.not_visited_count = not_visited_count;
    }
}
